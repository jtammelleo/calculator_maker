// Multiply
function multiply(a,b) { 
    return a * b
}

var x = multiply(8,9)

console.log("8 times 9 is " + x)

// Divide
function divide(a,b) { 
    return a / b
}

var y = divide(8,9)

console.log("8 divided by 9 is " + y)

// Add
function add(a,b) { 
    return a + b
}

var z = add(8,9)

console.log("8 plus 9 is " + z)

// Subtract
function subtract(a,b) { 
    return a - b
}

var s = subtract(8,9)

console.log("8 minus 9 is " + s)